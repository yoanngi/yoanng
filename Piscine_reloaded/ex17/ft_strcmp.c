int		ft_strcmp(char *s1, char *s2)
{
	int result;
	int i;

	result = 0;
	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
	{
		i++;
	}
	if (s1[i] < s2[i])
		return (-1);
	else if (s1[i] == s2[i])
		return (0);
	else
		return (1);
}

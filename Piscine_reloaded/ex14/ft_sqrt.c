int		ft_sqrt(int nb)
{
	int i;
	int c;

	i = 2;
	c = 0;
	while (i <= nb / 2 + 1)
	{
		c = i * i;
		if (c == nb)
			return (i);
		else
			i++;
	}
	return (0);
}

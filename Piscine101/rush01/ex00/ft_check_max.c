#include "sudoku.h"

int		ft_check_max(char *argv)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (argv[i])
	{
		if (argv[i] >= '1' && argv[i] <= '9')
			j++;
		i++;
	}
	if (i > 82)
		return (-150);
	return (j);
}

#include "sudoku.h"

int		ft_check_sudoku(char *argv)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (argv[i])
	{
		if (argv[i] == '.' || argv[i] == '\0')
			j++;
		if (argv[i] >= '1' && argv[i] <= '9')
			j++;
		i++;
	}
	if (j == 9)
		return (0);
	return (1);
}

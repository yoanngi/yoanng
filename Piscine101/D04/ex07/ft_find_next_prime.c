int		ft_find_next_prime(int nb)
{
	int bn;

	bn = nb;
	nb -= 1;
	if (bn <= 2)
		return (2);
	while (nb != 2)
	{
		if ((bn % nb) == 0)
		{
			return (ft_find_next_prime(bn + 1));
		}
		nb--;
	}
	return (bn);
}

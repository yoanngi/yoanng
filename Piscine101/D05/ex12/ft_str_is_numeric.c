int		ft_str_is_numeric(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] >= 48 && str[i] <= 57)
		{
			i++;
			j++;
		}
		else
			return (0);
	}
	if (i == j)
		return (1);
	else
		return (0);
}

int		ft_atoi(char *str)
{
	int bn;
	int nb;

	nb = 0;
	bn = 1;
	while ((*str == '\t') || (*str == '\n') || (*str == '\r') || (*str == '\v')
	|| (*str == '\f') || (*str == ' '))
		str++;
	if (*str == '-')
	{
		bn *= -1;
		str++;
	}
	if (*str == '+')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		nb *= 10;
		nb += (*str - 48);
		str++;
	}
	return (nb * bn);
}

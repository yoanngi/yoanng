int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	while (s1[i] || s2[i] || n != 0)
	{
		if (*s1 == *s2 && n == 0)
			return (*s1 - *s2);
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		n--;
		i++;
	}
	return (0);
}

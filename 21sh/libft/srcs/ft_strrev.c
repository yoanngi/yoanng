#include "../includes/libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	tmp;

	i = 0;
	j = ft_strlen(s) - 1;
	while (i < j / 2)
	{
		tmp = s[j - i];
		s[j - i] = s[i];
		s[i] = tmp;
		i++;
	}
	return (s);
}

#include "../../includes/shell.h"

void		ft_print_tab(char **tabl)
{
	int		i;

	i = 0;
	if (tabl == NULL)
		return ;
	while (tabl[i])
	{
		ft_putendl(tabl[i]);
		i++;
	}
}
